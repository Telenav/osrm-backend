package querynode

import (
	"reflect"
	"testing"
)

func TestWriteNodes(t *testing.T) {
	cases := []struct {
		p []byte
		n Nodes
	}{
		{
			[]byte{
				0x19, 0xc4, 0xd8, 0xf8, 0x8c, 0xdb, 0x59, 0x02, 0x85, 0xea, 0x98, 0x01, 0x00, 0x00, 0x00, 0x00,
			},
			Nodes{
				Node{FixedLon: -120011751, FixedLat: 39443340, ID: 26798725},
			},
		},
		{
			[]byte{
				0x19, 0xc4, 0xd8, 0xf8, 0x8c, 0xdb, 0x59, 0x02, 0x85, 0xea, 0x98, 0x01, 0x00, 0x00, 0x00, 0x00,
				0xFF, // redundant byte
			},
			Nodes{
				Node{FixedLon: -120011751, FixedLat: 39443340, ID: 26798725},
			},
		},
		{
			[]byte{
				0x19, 0xc4, 0xd8, 0xf8, 0x8c, 0xdb, 0x59, 0x02, 0x85, 0xea, 0x98, 0x01, 0x00, 0x00, 0x00, 0x00,
				0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, // redundant bytes
			},
			Nodes{
				Node{FixedLon: -120011751, FixedLat: 39443340, ID: 26798725},
			},
		},
		{
			[]byte{
				0x19, 0xc4, 0xd8, 0xf8, 0x8c, 0xdb, 0x59, 0x02, 0x85, 0xea, 0x98, 0x01, 0x00, 0x00, 0x00, 0x00,
				0x66, 0x03, 0x24, 0xf9, 0x6b, 0xba, 0x27, 0x02, 0x7b, 0x11, 0x86, 0x1e, 0x01, 0x00, 0x00, 0x00,
			},
			Nodes{
				Node{FixedLon: -120011751, FixedLat: 39443340, ID: 26798725},
				Node{FixedLon: -115080346, FixedLat: 36158059, ID: 4807070075},
			},
		},
	}

	for _, c := range cases {
		var n Nodes
		writeLen, err := n.Write(c.p)
		if err != nil {
			t.Error(err)
		}
		if len(c.p)-writeLen != len(c.p)%nodeBytes {
			t.Errorf("len(p) %d but write len %d", len(c.p), writeLen)
		}
		if !reflect.DeepEqual(n, c.n) {
			t.Errorf("parse %v expect %v but got %v", c.p, c.n, n)
		}
	}
}
